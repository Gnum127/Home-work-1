public class Summator {
    private long firstNumber; // задание переменных
    private long secondNumber;

    public void Summator() { // задание метода
    }

    public void setFirstNumber(long number) { // задание методов
        firstNumber = number;
    }

    public void setSecondNumber(long number) {
        secondNumber = number;
    }

    public long Sum() {
        return firstNumber + secondNumber;
    }
}
